#!/usr/bin/env python3
"""Rebuild platform_data/benchmarks/manifest.v1.yaml from local parquet datasets."""

from __future__ import annotations

import argparse
import hashlib
import json
import os
from dataclasses import dataclass
from datetime import datetime, timezone
from pathlib import Path


BENCHMARK_FILES: dict[str, str] = {
    "BTC": "BTC/daily.parquet",
    "SPY": "SPY/daily.parquet",
    "XAUUSD": "XAUUSD/daily.parquet",
    "DXY": "DXY/daily.parquet",
}


@dataclass(frozen=True)
class Coverage:
    start_ts: str | None
    end_ts: str | None
    row_count: int


def _repo_root() -> Path:
    return Path(__file__).resolve().parents[2]


def _library_root(repo_root: Path) -> Path:
    override = os.environ.get("INVARIANCE_BENCHMARK_LIBRARY_ROOT")
    if override:
        return Path(override).expanduser().resolve()
    return repo_root / "platform_data" / "benchmarks"


def _normalize_timestamp(value: object) -> str | None:
    if value is None:
        return None
    if hasattr(value, "isoformat"):
        return str(value.isoformat())
    return str(value)


def _read_coverage(dataset_path: Path) -> Coverage:
    try:
        import pyarrow.compute as pc
        import pyarrow.parquet as pq
    except Exception as exc:  # pragma: no cover - environment dependent
        raise RuntimeError(
            "pyarrow is required to rebuild benchmark manifest coverage; "
            f"install pyarrow in your Python environment. ({exc})"
        ) from exc

    parquet_file = pq.ParquetFile(str(dataset_path))
    row_count = int(parquet_file.metadata.num_rows)

    table = pq.read_table(str(dataset_path), columns=["ts"])
    if "ts" not in table.schema.names:
        raise RuntimeError(f"Missing required 'ts' column in dataset: {dataset_path}")

    non_null_ts = pc.drop_null(table["ts"])
    if len(non_null_ts) == 0:
        return Coverage(start_ts=None, end_ts=None, row_count=row_count)

    start_ts = _normalize_timestamp(pc.min(non_null_ts).as_py())
    end_ts = _normalize_timestamp(pc.max(non_null_ts).as_py())
    return Coverage(start_ts=start_ts, end_ts=end_ts, row_count=row_count)


def _build_revision(coverage_map: dict[str, Coverage]) -> str:
    canonical = {
        benchmark_id: {
            "file": BENCHMARK_FILES[benchmark_id],
            "start_ts": coverage.start_ts,
            "end_ts": coverage.end_ts,
            "row_count": coverage.row_count,
        }
        for benchmark_id, coverage in sorted(coverage_map.items())
    }
    digest = hashlib.sha256(json.dumps(canonical, sort_keys=True, separators=(",", ":")).encode("utf-8")).hexdigest()[:12]
    return f"v1-{digest}"


def _render_manifest_yaml(revision: str, generated_at: str, coverage_map: dict[str, Coverage]) -> str:
    lines: list[str] = [
        "# Auto-generated by scripts/benchmarks/rebuild_manifest.py",
        f"revision: '{revision}'",
        f"generated_at: '{generated_at}'",
        "benchmarks:",
    ]

    for benchmark_id in BENCHMARK_FILES:
        relative_file = BENCHMARK_FILES[benchmark_id]
        coverage = coverage_map[benchmark_id]
        lines.extend(
            [
                f"  - id: '{benchmark_id}'",
                f"    file: '{relative_file}'",
                "    frequency: '1d'",
                "    source: 'platform_managed'",
                "    coverage:",
                f"      start_ts: '{coverage.start_ts}'" if coverage.start_ts is not None else "      start_ts: null",
                f"      end_ts: '{coverage.end_ts}'" if coverage.end_ts is not None else "      end_ts: null",
                f"      row_count: {coverage.row_count}",
            ]
        )

    return "\n".join(lines) + "\n"


def main() -> int:
    parser = argparse.ArgumentParser(description="Rebuild benchmark library manifest from local parquet files.")
    parser.add_argument("--dry-run", action="store_true", help="Print generated manifest content without writing to disk.")
    args = parser.parse_args()

    repo_root = _repo_root()
    library_root = _library_root(repo_root)
    manifest_path = library_root / "manifest.v1.yaml"

    coverage_map: dict[str, Coverage] = {}
    for benchmark_id, relative_file in BENCHMARK_FILES.items():
        dataset_path = library_root / relative_file
        if not dataset_path.exists():
            print(f"ERROR: Missing benchmark dataset for {benchmark_id}: {dataset_path}")
            return 1

        try:
            coverage = _read_coverage(dataset_path)
        except Exception as exc:
            print(f"ERROR: Failed to inspect {benchmark_id} at {dataset_path}: {exc}")
            return 1

        coverage_map[benchmark_id] = coverage

    revision = _build_revision(coverage_map)
    generated_at = datetime.now(timezone.utc).replace(microsecond=0).isoformat().replace("+00:00", "Z")
    rendered = _render_manifest_yaml(revision=revision, generated_at=generated_at, coverage_map=coverage_map)

    print(f"Library root:   {library_root}")
    print(f"Manifest path:  {manifest_path}")
    print(f"Revision:       {revision}")
    print(f"Generated at:   {generated_at}")

    for benchmark_id in BENCHMARK_FILES:
        coverage = coverage_map[benchmark_id]
        print(
            f" - {benchmark_id}: file={BENCHMARK_FILES[benchmark_id]}, "
            f"start={coverage.start_ts}, end={coverage.end_ts}, rows={coverage.row_count}"
        )

    if args.dry_run:
        print("\n--- manifest.v1.yaml (dry-run) ---")
        print(rendered, end="")
        return 0

    manifest_path.parent.mkdir(parents=True, exist_ok=True)
    manifest_path.write_text(rendered, encoding="utf-8")
    print("\nManifest rebuilt successfully.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
